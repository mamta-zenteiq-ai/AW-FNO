#!/usr/bin/env python3
"""
Aggregate PDEBench Burgers Nu=0.001 results into a single comparison table.

Each row = one model trained on the shock-regime Burgers benchmark.
Metrics taken from the epoch with best test_rel_l2 (i.e., what `best.pt`
corresponds to).

Outputs:
  outputs/tables/burgers_main_table.csv
  outputs/tables/burgers_main_table.tex
  Pretty-printed console table.
"""

from __future__ import annotations

import csv
from pathlib import Path
from typing import Dict, List, Optional

ROOT = Path(__file__).resolve().parent.parent
HDD = Path("/media/HDD/mamta_backup/aw_fno_results")

RUNS: List[Dict] = [
    {"name": "FNO",                                       "dir": HDD / "fno_pdebench_burgers"},
    {"name": "WNO",                                       "dir": HDD / "wno_pdebench_burgers"},
    {"name": "AW-FNO (additive, v_F + v_W)",              "dir": HDD / "awfno_pdebench_burgers_additive"},
    {"name": "AW-FNO (fixed gate, α=0.5)",                "dir": HDD / "awfno_pdebench_burgers_no_gate"},
    {"name": "AW-FNO (1×1 adaptive gate, λ_ent=0.01)",    "dir": HDD / "awfno_pdebench_burgers"},
    {"name": "AW-FNO (rich gate k=5, 2L, λ_ent=0.01)",    "dir": HDD / "awfno_pdebench_burgers_richgate"},
]

METRICS = [
    ("test_rel_l2",          "Rel L2 ↓",      "{:.4e}"),
    ("test_rel_h1",          "Rel H1 ↓",      "{:.4e}"),
    ("test_high_freq_rel_l2","High-f L2 ↓",   "{:.3f}"),
    ("test_mse",             "MSE ↓",         "{:.4e}"),
    ("gate_entropy",         "Gate H",        "{:.4f}"),
]


def _read_best_row(metrics_csv: Path) -> Optional[Dict]:
    if not metrics_csv.exists():
        return None
    best = None
    with open(metrics_csv) as f:
        for row in csv.DictReader(f):
            try:
                v = float(row["test_rel_l2"])
            except (KeyError, ValueError):
                continue
            if best is None or v < float(best["test_rel_l2"]):
                best = row
    return best


def _fmt(val, spec: str) -> str:
    if val in ("", "nan", "NaN", None):
        return "—"
    try:
        return spec.format(float(val))
    except (ValueError, TypeError):
        return str(val)


def main() -> None:
    out_dir = ROOT / "outputs" / "tables"
    out_dir.mkdir(parents=True, exist_ok=True)

    rows = []
    for run in RUNS:
        csv_path = run["dir"] / "metrics.csv"
        best = _read_best_row(csv_path)
        if best is None:
            rows.append({"name": run["name"], "missing": True, "dir": str(run["dir"])})
            continue
        row = {"name": run["name"], "epoch": int(float(best["epoch"]))}
        for key, _, _ in METRICS:
            row[key] = best.get(key, "")
        row["dir"] = str(run["dir"])
        rows.append(row)

    widths = {"name": max(len(r["name"]) for r in rows) + 2, "Epoch": 7}
    for _, label, _ in METRICS:
        widths[label] = max(len(label), 13)

    total_w = widths["name"] + widths["Epoch"] + sum(widths[lbl] for _, lbl, _ in METRICS)
    sep = "=" * total_w
    print()
    print(sep)
    print("  PDEBench Burgers Nu=0.001 — Main Comparison Table (best-epoch metrics)")
    print(sep)
    header = f"{'Model':<{widths['name']}}{'Epoch':>{widths['Epoch']}}"
    for _, label, _ in METRICS:
        header += f"{label:>{widths[label]}}"
    print(header)
    print("-" * total_w)
    for r in rows:
        if r.get("missing"):
            print(f"{r['name']:<{widths['name']}}  [missing: {r['dir']}]")
            continue
        line = f"{r['name']:<{widths['name']}}{r['epoch']:>{widths['Epoch']}}"
        for key, label, spec in METRICS:
            line += f"{_fmt(r[key], spec):>{widths[label]}}"
        print(line)
    print(sep)
    print()

    csv_out = out_dir / "burgers_main_table.csv"
    with open(csv_out, "w", newline="") as f:
        fieldnames = ["name", "epoch"] + [k for k, _, _ in METRICS] + ["dir"]
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        for r in rows:
            if r.get("missing"):
                w.writerow({"name": r["name"], "dir": r["dir"]})
            else:
                w.writerow({k: r.get(k, "") for k in fieldnames})
    print(f"CSV: {csv_out}")

    tex_out = out_dir / "burgers_main_table.tex"
    with open(tex_out, "w") as f:
        f.write("% Auto-generated by scripts/aggregate_burgers_results.py\n")
        f.write("\\begin{tabular}{l" + "c" * len(METRICS) + "}\n")
        f.write("\\toprule\n")
        f.write("Model & " + " & ".join(label for _, label, _ in METRICS) + " \\\\\n")
        f.write("\\midrule\n")
        for r in rows:
            if r.get("missing"):
                continue
            cells = [r["name"].replace("α", "$\\alpha$").replace("λ", "$\\lambda$")]
            for key, _, spec in METRICS:
                cells.append(_fmt(r[key], spec))
            f.write(" & ".join(cells) + " \\\\\n")
        f.write("\\bottomrule\n")
        f.write("\\end{tabular}\n")
    print(f"LaTeX: {tex_out}")


if __name__ == "__main__":
    main()
